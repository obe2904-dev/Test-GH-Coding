async function fetchBusinessData(
  supabaseClient: ReturnType<typeof createClient>,
  businessId: string,
  language: string
): Promise<{
  business: any
  primaryLocation: any
  postalCode: string | null
  existingProfile: any
  businessProfile: any
  menuResultsRaw: any[]
  menuResultsWithDishes: any[]
  location: any
  operations: any
  openingHours: any[]
  userProfile: any
}> {
  // STEP 1: Fetch business first to get owner_id
  const { data: businessData, error: businessError } = await supabaseClient
    .from('businesses')
    .select('*, local_location_reference, business_locations!inner(postal_code, city)')
    .eq('id', businessId)
    .eq('business_locations.is_primary', true)
    .single()

  if (businessError || !businessData) {
    throw new Error(`Business not found: ${businessError?.message || 'Unknown error'}`)
  }

  // STEP 2: Fetch everything else in parallel, including user profile using owner_id
  const [
    { data: existingProfile },
    { data: businessProfile },
    { data: menuResultsRaw, error: menuFetchError },
    { data: location, error: locationError },
    { data: operations },
    { data: openingHours },
    { data: menuResultsWithDishes },
    { data: userProfile }
  ] = await Promise.all([
    supabaseClient
      .from('business_brand_profile')
      .select('tone_of_voice, tone_keywords, tone_model, typical_openings, things_to_avoid, voice_constraints, target_audience, communication_goal, emotional_promise, brand_context, recognizable_interior_identity, visual_character, venue_scene, humor_level, menu_overview_summary, gastronomic_profile, signature_themes, strategic_audience_segments, business_character')
      .eq('business_id', businessId)
      .single(),
    supabaseClient
      .from('business_profile')
      .select('long_description, booking_url')
      .eq('business_id', businessId)
      .maybeSingle(),
    supabaseClient
      .from('menu_results_v2')
      .select('id, business_id, ai_summary, structured_data, service_periods, service_period_name, completed_at, source_url, status, language_code, menu_type, time_start, time_end, time_source, time_confirmed, menu_sources!menu_results_v2_source_id_fkey(label, menu_type)')
      .eq('business_id', businessId)
      .eq('status', 'done')
      // CRITICAL FIX: Fetch ALL language variants — menu_results_v2 is the truth
      // If user uploaded a cocktails menu (even in English), it MUST be detected
      // The language filter was incorrectly excluding menus with different language_code
      .order('completed_at', { ascending: false }),
    supabaseClient
      .from('business_location_intelligence')
      .select('neighborhood, neighborhood_character, area_type, category_scores, demographic_proximity, who, traffic_rhythm, physical_context, raw_competitive_venues, landmarks_nearby, nearby_hospitality, category_modifiers, location_marketing_hooks, local_location_reference, concept_fit_by_category, latitude, longitude')
      .eq('business_id', businessId)
      .maybeSingle(),
    supabaseClient
      .from('business_operations')
      .select('*')
      .eq('business_id', businessId)
      .single(),
    supabaseClient
      .from('opening_hours')
      .select('*')
      .eq('business_id', businessId),
    supabaseClient
      .from('menu_results_v2')
      .select('representative_dishes, language_code, service_period_name')
      .eq('business_id', businessId)
      .eq('status', 'done')
      .not('representative_dishes', 'is', null)
      .order('completed_at', { ascending: false }),
    supabaseClient
      .from('profiles')
      .select('selected_platforms')
      .eq('id', businessData.owner_id)  // ✅ FIX: Use owner_id from business, not business_id
      .maybeSingle()
  ])
  
  if (locationError) {
    console.warn('⚠️ Location intelligence query failed — proceeding without it')
  }
  if (menuFetchError) {
    console.warn('⚠️ Menu fetch error:', menuFetchError)
  }

  return {
    business: businessData,
    primaryLocation: businessData.business_locations?.[0] || null,
    postalCode: businessData.business_locations?.[0]?.postal_code || null,
    existingProfile: existingProfile || null,
    businessProfile: businessProfile || null,
    menuResultsRaw: menuResultsRaw || [],
    menuResultsWithDishes: menuResultsWithDishes || [],
    location: location || null,
    operations: operations || null,
    openingHours: openingHours || [],
    userProfile: userProfile || null
  }
}

