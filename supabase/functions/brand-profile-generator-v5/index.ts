/**
 * Determine price positioning from average price
 * Based on Danish restaurant market 2026
 */
function determinePricePositioning(avgPrice?: number): 'budget' | 'value' | 'moderate' | 'upscale' | 'premium' {
  if (!avgPrice) return 'moderate';
  
  // Danish market pricing (DKK):
  // Budget: < 100 (fast casual, quick service)
  // Value: 100-150 (casual dining, good quality-price ratio)
  // Moderate: 150-220 (mid-range restaurants, quality focus)
  // Upscale: 220-350 (fine-casual, elevated dining)
  // Premium: > 350 (fine dining, destination restaurants)
  
  if (avgPrice < 100) return 'budget';
  if (avgPrice < 150) return 'value';
  if (avgPrice < 220) return 'moderate';
  if (avgPrice < 350) return 'upscale';
  return 'premium';
}

/**
 * Extract strategic audience segments for persona generation
 * Flattens primary + secondary segments from verbose audience_segments JSON
 * 
 * @param segments - Full audience segments array from audienceProfile
 * @returns Compact strategic segments object or null
 */
function extractStrategicSegments(segments: Array<{ 
  label: string; 
  segment_size?: 'primary' | 'secondary' | 'niche';
  timing_preference?: string;
  timing_windows?: string[];
}>): any {
  if (!segments || segments.length === 0) return null;
  
  const primary = segments.find(seg => seg.segment_size === 'primary');
  const secondary = segments.filter(seg => seg.segment_size === 'secondary');
  
  const formatTiming = (seg: typeof segments[0]) => {
    // NOTE: As of June 28, 2026 - timing_windows removed from AudienceSegment interface
    // Segments are now occasion-based rather than time-slot based
    return null;
  };
  
  return {
    primary: primary ? {
      id: primary.people_type?.toLowerCase().replace(/\s+/g, '_').replace(/æ/g, 'ae').replace(/ø/g, 'o').replace(/å/g, 'aa'),
      name: primary.people_type,
      timing: formatTiming(primary)
    } : null,
    secondary: secondary.length > 0 ? secondary.map(seg => ({
      id: seg.people_type?.toLowerCase().replace(/\s+/g, '_').replace(/æ/g, 'ae').replace(/ø/g, 'o').replace(/å/g, 'aa'),
      name: seg.people_type,
      timing: formatTiming(seg)
    })) : null
  };
}

/**
 * Extract tone-relevant demographic signals from audience segments
 * 
 * CRITICAL: Student/tourist location scores ≠ guest demographics!
 * - Student 88 = geography (Aarhus is student town), NOT guest profile
 * - Only use demographic for tone if it's a PRIMARY audience segment
 * - Student tone only relevant if price is budget/value
 * 
 * @param demographicScores - Location-based demographic scores
 * @param programmes - Actual audience segments per programme
 * @param pricePositioning - Price tier from menu analysis
 */
function extractToneRelevantDemographics(
  demographicScores: Record<string, number>,
  programmes: Array<{ audienceProfile: { audience_segments: Array<{ label: string; segment_size: string }> } }>,
  pricePositioning: 'budget' | 'value' | 'moderate' | 'upscale' | 'premium'
): { primary_demographic?: string; score?: number; tone_implications?: string[] } | undefined {
  
  // Check if student/tourist appear as PRIMARY segments in ANY programme
  const hasPrimaryStudent = programmes.some(p => 
    p.audienceProfile.audience_segments.some(seg => 
      seg.segment_size === 'primary' && 
      /student|studerende/i.test(seg.people_type)
    )
  );
  
  const hasPrimaryTourist = programmes.some(p =>
    p.audienceProfile.audience_segments.some(seg =>
      seg.segment_size === 'primary' &&
      /tourist|turister/i.test(seg.people_type)
    )
  );
  
  // Build candidate list based on PRIMARY segments + price relevance
  const candidates: Array<{ demo: string; score: number; relevant: boolean }> = [];
  
  if (hasPrimaryStudent && (pricePositioning === 'budget' || pricePositioning === 'value')) {
    candidates.push({
      demo: 'student',
      score: demographicScores['student'] || 0,
      relevant: true
    });
  }
  
  if (hasPrimaryTourist) {
    candidates.push({
      demo: 'tourist', 
      score: demographicScores['tourist'] || 0,
      relevant: true
    });
  }
  
  // Return highest-scoring RELEVANT demographic
  const sorted = candidates
    .filter(c => c.relevant && c.score >= 70)
    .sort((a, b) => b.score - a.score);
  
  if (sorted.length === 0) {
    console.log('[Demographics] No PRIMARY student/tourist segments found, or price tier incompatible → tone demographics = null');
    return undefined;
  }
  
  const primary = sorted[0];
  
  // Add tone implications
  const implications: string[] = [];
  if (primary.demo === 'student') {
    implications.push('Prisbevidst målgruppe kræver tilgængelig tone');
    implications.push('Casual, no-fuss kommunikation');
    implications.push('Værdifokus over premium-positionering');
  } else if (primary.demo === 'tourist') {
    implications.push('Visual appeal vigtig (tourist decision-making)');
    implications.push('Location references critical');
    implications.push('International-friendly sprog');
  }
  
  console.log(`[Demographics] PRIMARY ${primary.demo} segment detected (score ${primary.score}) → tone implications added`);
  
  return {
    primary_demographic: primary.demo,
    score: primary.score,
    tone_implications: implications
  };
}

