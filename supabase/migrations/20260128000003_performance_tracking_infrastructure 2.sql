-- Function to log performance from API
CREATE OR REPLACE FUNCTION log_post_performance(
  p_business_id UUID,
  p_post_idea_id UUID,
  p_content_type TEXT,
  p_platform TEXT,
  p_posted_at TIMESTAMPTZ,
  p_reach INTEGER,
  p_engagement INTEGER,
  p_likes INTEGER DEFAULT 0,
  p_comments INTEGER DEFAULT 0,
  p_shares INTEGER DEFAULT 0,
  p_saves INTEGER DEFAULT 0,
  p_clicks INTEGER DEFAULT 0
)
RETURNS UUID AS $$
DECLARE
  v_log_id UUID;
  v_business_type TEXT;
BEGIN
  -- Get business type
  SELECT bo.establishment_type INTO v_business_type
  FROM business_operations bo
  WHERE bo.business_id = p_business_id;
  
  -- Insert performance log
  INSERT INTO content_performance_log (
    business_id,
    post_idea_id,
    content_type,
    business_type,
    platform,
    posted_at,
    post_time,
    post_day_of_week,
    reach,
    impressions,
    engagement_total,
    likes,
    comments,
    shares,
    saves,
    clicks
  )
  VALUES (
    p_business_id,
    p_post_idea_id,
    p_content_type,
    v_business_type,
    p_platform,
    p_posted_at,
    p_posted_at::TIME,
    EXTRACT(DOW FROM p_posted_at)::INTEGER,
    p_reach,
    p_reach, -- Use reach as proxy for impressions if not provided
    p_engagement,
    p_likes,
    p_comments,
    p_shares,
    p_saves,
    p_clicks
  )
  RETURNING id INTO v_log_id;
  
  RETURN v_log_id;
END;
$$ LANGUAGE plpgsql;

